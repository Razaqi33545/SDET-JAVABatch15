package assignment01;

public class DecimalNumber {
    public static void main(String[] args) {

        double number1 = 200.50;
        double number2 = 100.50;

        System.out.println("The addition of 2 Numbers " + number1 + " and " + number2 + " Equal to " + (number1+number2));

        System.out.println("The subtract of 2 Numbers " + number1 + " and " + number2 + " Equal to " + (number1-number2));

        System.out.println("The multiplication of 2 Numbers " + number1 + " and " + number2 + " Equal to " + (number1*number2));

        System.out.println("The division of 2 Numbers " + number1 + " and " + number2 + " Equal to " + (number1/number2));

        System.out.println("The remainder of 2 Numbers " + number1 + " and " + number2 + " Equal to " + (number1%number2));
    }
}
