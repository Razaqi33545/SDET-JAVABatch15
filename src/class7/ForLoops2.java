package class7;

public class ForLoops2 {
    public static void main(String[] args) {

        for (int i = 0; i < 20; i++) {
            if (i%3!=0){ // this is remainder not division that is why 3,6,9,12,15,18 are not in the console.
                System.out.println(i);
            }
        }

    }
}
