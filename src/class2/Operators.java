package class2;

public class Operators {
    public static void main(String[] args) {

        int number1=10;
        int number2=20;

        System.out.println(number1+number2);

        int x = 3;
        int y = 2;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);


    }
}
