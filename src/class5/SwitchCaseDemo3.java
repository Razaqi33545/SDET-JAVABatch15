package class5;

public class SwitchCaseDemo3 {
    public static void main(String[] args) {

        String day ="Tuesday";

        switch (day){
            case "Monday":
                System.out.println("01/09/23");
                break;

            case "Tuesday":
                System.out.println("01/10/23");
                break;

            case "Wednesday":
                System.out.println("01/11/23");
                break;

            case "Thursday":
                System.out.println("01/12/23");
                break;

            case "Friday":
                System.out.println("01/13/23");
                break;

            case "Saturday":
                System.out.println("01/14/23");
                break;

            case "Sunday":
                System.out.println("01/15/23");
                break;

            default:
                System.out.println("No Date Available");
        }














    }
}
